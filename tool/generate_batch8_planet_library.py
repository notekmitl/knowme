def slug(text: str) -> str:
    t = unicodedata.normalize("NFC", text.strip())
    t = re.sub(r"[^\w\u0E00-\u0E7F]+", "_", t, flags=re.UNICODE)
    t = re.sub(r"_+", "_", t).strip("_")
    if not t:
        t = "x"
    if t[0].isdigit():
        t = "n" + t
    return t[:56]


entities: dict[str, dict] = {}
units: list[dict] = []
used_ids: set[str] = set()


def add(planet: str, category: str, thai: str, page: str) -> None:
    thai = thai.strip()
    if not thai:
        return
    eid = f"attribute.{category}.{slug(thai)}"
    if eid not in entities:
        entities[eid] = {
            "id": eid,
            "thai": thai,
            "parentId": CAT[category],
            "category": "attribute",
        }
    base = f"mahabhut.p{page}.{planet}_relates_{eid.replace('.', '_')}"
    uid = base
    n = 2
    while uid in used_ids:
        uid = f"{base}_{n}"
        n += 1
    used_ids.add(uid)
    units.append(
        {
            "id": uid,
            "subject": PLANET[planet],
            "relation": "relates_to",
            "object": eid,
            "objectKind": "keyword",
            "page": page,
        }
    )


