import 'models/thai_mirror_section_id.dart';

/// Deterministic narrative template variants for population-scale diversity.
abstract final class ThaiMirrorNarrativeVariants {
  static int indexFor({
    required ThaiMirrorSectionId sectionId,
    required String seed,
    required int variantCount,
  }) {
    if (variantCount <= 1) return 0;
    return Object.hash(sectionId, seed).abs() % variantCount;
  }

  static String coreSelfOpening(int variant) {
    return switch (variant % 4) {
      0 => 'ตัวตนของคุณอาจสะท้อนผ่าน',
      1 => 'ภาพที่คุณมองตัวเองอาจเชื่อมกับ',
      2 => 'แก่นแท้ของคุณอาจปรากฏผ่าน',
      _ => 'บางครั้งตัวตนที่คุณรู้สึกอาจสะท้อนผ่าน',
    };
  }

  static String coreSelfClosing(int variant) {
    return switch (variant % 4) {
      0 => 'หลายครั้งคุณอาจรู้สึกว่าแพทเทิร์นนี้ปรากฏเมื่อต้องตัดสินใจเรื่องสำคัญ',
      1 => 'คุณอาจสังเกตว่าแพทเทิร์นนี้โผล่ขึ้นเมื่อต้องเลือกทิศทางชีวิต',
      2 => 'แพทเทิร์นนี้อาจชัดขึ้นเมื่อคุณต้องยืนยันว่าต้องการอะไรจริง ๆ',
      _ => 'หลายครั้งคุณอาจเห็นแพทเทิร์นนี้เมื่อต้องตัดสินใจเรื่องที่สำคัญกับคุณ',
    };
  }

  static String thinkingOpening(int variant) {
    return switch (variant % 4) {
      0 => 'รูปแบบการคิดของคุณมักเชื่อมกับ',
      1 => 'วิธีที่คุณประมวลผลข้อมูลอาจสะท้อนผ่าน',
      2 => 'การตัดสินใจของคุณมักมีแนวโน้มเชื่อมกับ',
      _ => 'มุมมองเชิงวิเคราะห์ของคุณอาจสะท้อนผ่าน',
    };
  }

  static String thinkingClosing(int variant) {
    return switch (variant % 4) {
      0 => 'คุณอาจใช้แนวทางนี้เมื่อประมวลผลข้อมูลหรือเลือกทิศทาง',
      1 => 'หลายครั้งคุณอาจพึ่งแพทเทิร์นนี้เมื่อต้องจัดลำดับความสำคัญ',
      2 => 'คุณมักจะกลับมาใช้แนวทางนี้เมื่อต้องเลือกทางเดิน',
      _ => 'แนวทางนี้อาจปรากฏเมื่อคุณต้องแปลงข้อมูลให้เป็นการตัดสินใจ',
    };
  }

  static String emotionalOpening(int variant) {
    return switch (variant % 4) {
      0 => 'โลกอารมณ์ของคุณอาจสัมผัสได้ผ่าน',
      1 => 'แรงรู้สึกภายในของคุณมักเชื่อมกับ',
      2 => 'สภาวะอารมณ์ของคุณอาจสะท้อนผ่าน',
      _ => 'บางครั้งอารมณ์ที่คุณรู้สึกอาจเชื่อมกับ',
    };
  }

  static String emotionalClosing(int variant) {
    return switch (variant % 4) {
      0 => 'บางครั้งแรงจูงใจและความรู้สึกอาจเคลื่อนไหวตามแพทเทิร์นนี้',
      1 => 'คุณอาจสังเกตว่าความรู้สึกเปลี่ยนตามจังหวะของแพทเทิร์นนี้',
      2 => 'อารมณ์ของคุณอาจขึ้นลงตามแรงจูงใจที่สะท้อนจากแพทเทิร์นนี้',
      _ => 'หลายครั้งความรู้สึกอาจนำทางคุณผ่านแพทเทิร์นนี้',
    };
  }

  static String relationshipsOpening(int variant) {
    return switch (variant % 4) {
      0 => 'ในความสัมพันธ์คุณอาจแสดงออกผ่าน',
      1 => 'เวลาอยู่ใกล้ผู้อื่นคุณอาจสะท้อนผ่าน',
      2 => 'ในมิติความสัมพันธ์คุณมักแสดงผ่าน',
      _ => 'บางครั้งคุณอาจสร้างความใกล้ชิดผ่าน',
    };
  }

  static String relationshipsClosing(int variant) {
    return switch (variant % 4) {
      0 => 'คุณมีแนวโน้มนำคุณลักษณะนี้ไปใช้เมื่อสร้างความใกล้ชิดกับผู้อื่น',
      1 => 'แพทเทิร์นนี้อาจปรากฏเมื่อคุณต้องการความเข้าใจจากคนรอบข้าง',
      2 => 'คุณอาจใช้แพทเทิร์นนี้เมื่อต้องสร้างความไว้วางใจ',
      _ => 'หลายครั้งคุณอาจแสดงตนในเชิงความสัมพันธ์ผ่านแพทเทิร์นนี้',
    };
  }

  static String workOpening(int variant) {
    return switch (variant % 4) {
      0 => 'ด้านงานและความทะเยอทะยานคุณอาจมองผ่าน',
      1 => 'ในมุมงานและเป้าหมายคุณมักสะท้อนผ่าน',
      2 => 'เวลาลงมือทำเรื่องสำคัญคุณอาจมองผ่าน',
      _ => 'แรงขับในการทำงานของคุณอาจเชื่อมกับ',
    };
  }

  static String workClosing(int variant) {
    return switch (variant % 4) {
      0 => 'คุณมักจะใช้แนวทางนี้เมื่อวางแผนหรือลงมือทำในสิ่งที่สำคัญ',
      1 => 'แพทเทิร์นนี้อาจช่วยคุณเมื่อต้องกำหนดทิศทางงาน',
      2 => 'คุณอาจพึ่งแพทเทิร์นนี้เมื่อต้องแปลงเป้าหมายเป็นการลงมือทำ',
      _ => 'หลายครั้งคุณอาจใช้แนวทางนี้เมื่อต้องผลักดันสิ่งที่สำคัญ',
    };
  }

  static String strengthsIntro(int variant) {
    return switch (variant % 4) {
      0 => 'จุดแข็งที่คุณอาจสังเกตได้ ได้แก่',
      1 => 'หลายครั้งจุดแข็งที่โดดเด่น ได้แก่',
      2 => 'สิ่งที่คุณอาจทำได้ดีเมื่อมองย้อนกลับ ได้แก่',
      _ => 'แนวโน้มที่คุณอาจเห็นเป็นจุดแข็ง ได้แก่',
    };
  }

  static String growthAreasIntro(int variant) {
    return switch (variant % 4) {
      0 => 'พื้นที่ที่คุณอาจอยากพัฒนา เช่น',
      1 => 'บางครั้งพื้นที่ที่น่าสำรวจเพิ่ม เช่น',
      2 => 'มุมที่คุณอาจอยากปรับสมดุล เช่น',
      _ => 'แนวทางที่คุณอาจพัฒนาต่อ เช่น',
    };
  }

  static String growthPathIntro(int variant) {
    return switch (variant % 4) {
      0 => 'เส้นทางเติบโตที่อาจเหมาะกับคุณ คือ',
      1 => 'ทิศทางที่คุณอาจเติบโตต่อไป คือ',
      2 => 'แนวทางพัฒนาที่อาจสอดคล้องกับคุณ คือ',
      _ => 'เส้นทางที่คุณอาจลองสำรวจ คือ',
    };
  }

  static String growthPathFallback(int variant) {
    return switch (variant % 4) {
      0 =>
        'เส้นทางเติบโตของคุณอาจเริ่มจากการสังเกตตนเองเล็ก ๆ น้อย ๆ และค่อย ๆ ปรับแนวทางให้เหมาะกับจังหวะชีวิต',
      1 =>
        'คุณอาจเริ่มเติบโตจากการสังเกตแพทเทิร์นเล็ก ๆ แล้วค่อย ๆ ปรับให้เหมาะกับตัวเอง',
      2 =>
        'การเติบโตอาจเริ่มจากก้าวเล็ก ๆ ที่ทำซ้ำอย่างตั้งใจจนเห็นความเปลี่ยนแปลง',
      _ =>
        'บางครั้งเส้นทางเติบโตอาจเริ่มจากการฟังตัวเองและปรับจังหวะให้เหมาะกับชีวิต',
    };
  }

  static String growthAreasFallback(int variant) {
    return switch (variant % 4) {
      0 =>
        'บางครั้งคุณอาจพบพื้นที่ที่อยากพัฒนาเมื่อมองตนเองอย่างเปิดใจ ซึ่งเป็นกระบวนการต่อเนื่องไม่ใช่ข้อจำกัดถาวร',
      1 =>
        'คุณอาจเห็นพื้นที่ที่อยากปรับเมื่อมองตัวเองอย่างเมตตา ซึ่งเป็นโอกาสเรียนรู้ไม่ใช่ข้อตำหนิ',
      2 =>
        'หลายครั้งพื้นที่พัฒนาอาจปรากฏเมื่อคุณยอมรับว่าการเติบโตเป็นกระบวนการต่อเนื่อง',
      _ =>
        'บางครั้งการมองจุดที่อยากพัฒนาอาจช่วยให้คุณเข้าใจตัวเองลึกขึ้นโดยไม่ต้องตัดสินตัวเอง',
    };
  }
}
